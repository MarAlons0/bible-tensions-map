def make_tension_entry(score_note_tuple):
    if score_note_tuple is None:
        return None
    score, note = score_note_tuple
    return {"score": score, "note": note}


def build_seed():
    books_out = {}
    for book_id, book in BOOKS.items():
        tensions_out = {}
        for tid, val in book["tensions"].items():
            tensions_out[tid] = val  # already a dict with score/note
        books_out[book_id] = {
            "name": book["name"],
            "section": book["section"],
            "chapters": book["chapters"],
            "dating": book["dating"],
            "sources": book["sources"],
            "summary": book["summary"],
            "tensions": tensions_out,
            "conduct": book["conduct"],
        }
    return {
        "tensions": TENSIONS,
        "conduct_categories": CONDUCT_CATEGORIES,
        "books": books_out,
    }


